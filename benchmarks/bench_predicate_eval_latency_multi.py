"""Per-event predicate evaluation latency under mixed source load.

NOT TTFAE -- this measures the predicate engine's per-event
evaluation cost, isolated from broadcast, SQLite, and subscriber
overhead. The bench answers: "for a typical 3-way multi-source
predicate, how much CPU does waitbus burn per frame the
broadcaster ships?"

The result feeds two downstream uses:

1. Regression-gate the predicate engine's hot path. A
   sub-microsecond evaluation becoming a multi-microsecond
   evaluation would silently inflate the higher-level cross-source
   wait latency without surfacing in the per-source TTFAE benches.
2. Provide a per-event-CPU figure that the predicate engine's
   documentation can cite alongside the TTFAE figure (the
   per-event cost is what an operator actually pays per event
   delivered to a subscriber with an active predicate).

Methodology
-----------
- Predicate: a real 3-way OR over ``source`` constructed via
  :func:`waitbus._predicate.parse_match` -- the same code
  path ``waitbus wait --match source=X --match source=Y --match
  source=Z`` uses. We deliberately reuse the production
  construction surface; bypassing it (e.g. hand-rolling a
  ``Predicate(...)`` literal) would measure a different, less
  realistic hot path.
- Frames: synthetic, generated by :func:`_make_frame`. Distribution
  is 25%/25%/25%/25% across the four sources waitbus supports;
  ordering is randomized with a fixed seed for reproducibility.
- Timing: ``time.monotonic_ns()``. Intra-process; bench is pure
  CPU.
- Wall-clock: sub-second per phase. The HdrHistogram's 1 ns floor
  is load-bearing because per-call cost can be tens of
  nanoseconds.

Note on clock overhead
----------------------
At this resolution the cost of ``time.monotonic_ns()`` itself
(~25-50 ns on Linux per call) is comparable to the predicate
evaluation cost. The measurement is therefore "predicate evaluation
+ two monotonic_ns calls + HdrHistogram record_value." This is
acceptable for regression-gating (the absolute number is less
useful than the slope across versions); to read off an "intrinsic"
predicate-only cost the reader subtracts a no-op-loop baseline
that is intentionally NOT included here, because doing so would
invite an HN reviewer to argue the subtraction was tuned to
flatter waitbus. The methodology document calls this out.

Sample posture
--------------
N=5000 + 500 warmup. No open-loop scheduler -- this is a pure CPU
bench, no I/O, so the scheduler's CO discipline does not apply
(there is no producer-consumer pair to coordinate). The loop
simply iterates as fast as it can.

Invocation
----------
::

    # Smoke (<1 second)
    uv run python -m benchmarks.bench_predicate_eval_latency_multi --smoke

    # Production baseline
    taskset -c 2,3 uv run python -m benchmarks.bench_predicate_eval_latency_multi \\
        --output benchmarks/baselines/predicate_eval_latency_multi.json
"""

from __future__ import annotations

import argparse
import random
import sys
import time
from pathlib import Path
from typing import Final

from benchmarks._bench_shared import FRAME_GEN_SEED as _SHARED_FRAME_GEN_SEED
from waitbus._predicate import Predicate, parse_match

from ._harness import (
    HdrRecorder,
    check_regression,
    collect_result,
    environment_report,
    gc_disabled,
    resolve_output_path,
    write_result,
)

_BENCH_NAME = "predicate_eval_latency_multi"
_DEFAULT_N = 5000
_DEFAULT_WARMUP = 500
_SMOKE_N = 500
_SMOKE_WARMUP = 50
_BASELINE_PATH = Path(__file__).resolve().parent / "baselines" / f"{_BENCH_NAME}.json"
_RESULTS_DIR = Path(__file__).resolve().parent / "results"

_SOURCES: tuple[str, ...] = ("github", "pytest", "docker", "fs")

# Re-export the shared constant under the bench-local name so call sites
# within this module are unchanged and the single-source-of-truth is
# ``benchmarks._bench_shared.FRAME_GEN_SEED``.
FRAME_GEN_SEED: Final[int] = _SHARED_FRAME_GEN_SEED
_FRAME_GEN_SEED = FRAME_GEN_SEED  # legacy alias used in this module

_DEFAULT_POOL_SIZE_FLOOR: Final[int] = 512
"""Minimum frame-pool size for the plugin-source parity check.

The pool is sized to ``max(n + warmup, _DEFAULT_POOL_SIZE_FLOOR)`` so
short smoke runs still exercise a diverse set of frames rather than
cycling over a tiny pool.
"""

_WIDE_POOL_SIZE_FLOOR: Final[int] = 1024
"""Minimum frame-pool size for the main bench's gc-on + gc-off runs.

Larger than ``_DEFAULT_POOL_SIZE_FLOOR`` to ensure the full N+warmup
budget (up to 5500 frames) can be served without heavy repetition
across the pre-built pool.
"""

_SOURCE_COUNT: Final[int] = 4
"""Number of distinct sources in ``_SOURCES``.

Used when computing the per-source frame count from the pool size so
the intent ``pool_size // _SOURCE_COUNT`` is self-documenting rather
than an unexplained ``// 4``.
"""

_P99_FRACTION: Final[float] = 0.99
"""Percentile fraction for the p99 regression gate and the plugin-parity
ceiling computation.

Named here so the value is the single source of truth for every
``value_at_percentile_fraction(0.99)`` call in this module.
"""

# Plugin-source latency parity invariant: registered-source frames must be
# predicate-matchable within 25% of built-in-source latency at p99. This
# constant is the tolerance expressed as a fraction (0.25 = 25%).
_PLUGIN_SOURCE_LATENCY_TOLERANCE = 0.25


def _make_frame(rng: random.Random, source: str) -> dict[str, object]:
    """Build one synthetic broadcast frame matching the waitbus shape.

    The shape is the same dict the broadcaster ships:
    ``{"source": ..., "event_type": ..., "owner": ..., "repo": ...,
    "event_id": ..., "fields": {...}}``. The ``fields`` dict carries
    per-source payload data the predicate may dig into. Enough
    fields are populated so ``_dig`` does some real work; field
    values are randomized so the bench does not benefit from CPU
    branch-prediction warmup against a constant frame.
    """
    fields: dict[str, object] = {
        "conclusion": rng.choice(("success", "failure", "skipped")),
        "head_sha": f"{rng.randrange(2**64):016x}",
        "run_id": rng.randrange(1_000_000),
    }
    if source == "pytest":
        fields["outcome"] = rng.choice(("passed", "failed", "skipped"))
    elif source == "docker":
        fields["exit_code"] = rng.choice((0, 1, 137))
    elif source == "fs":
        fields["event_type"] = rng.choice(("created", "modified", "deleted"))
    return {
        "source": source,
        "event_type": f"{source}_event",
        "owner": "bench",
        "repo": "predicate-eval",
        "event_id": f"01HK{rng.randrange(2**60):015X}",
        "fields": fields,
    }


def _build_predicate() -> Predicate:
    """Construct a 3-way OR-on-source predicate via production parse_match.

    The bench's representative predicate matches frames whose source
    is one of {github, pytest, docker}. Why three of four: realistic
    operators wait on a subset, not the full set; choosing 3 lets
    25% of frames in our synthetic stream be "negative" cases so
    the predicate's short-circuit path is exercised too.
    """
    return parse_match(['source="github"', 'source="pytest"', 'source="docker"'])


def _run_loop(*, frames: list[dict[str, object]], n: int, warmup: int, hdr: HdrRecorder) -> None:
    """Inner measurement loop: evaluate the predicate against each frame and record latency.

    Frames are pre-built so allocation cost is paid before the
    timed loop. The bench iterates ``n + warmup`` times; the first
    ``warmup`` samples are discarded.

    Per-iteration timing is two ``time.monotonic_ns()`` calls
    bracketing one ``predicate.evaluate(frame)`` call. ``frame`` is
    chosen round-robin from the pre-built list; the result of
    ``evaluate`` is unused (Python is fast enough that a no-op
    branch on the bool would dominate).
    """
    pred = _build_predicate()
    pred_evaluate = pred.evaluate  # local-bind for ~one less attribute lookup per iter
    monotonic_ns = time.monotonic_ns
    record = hdr.record
    n_frames = len(frames)

    # Total iterations: n + warmup. Pre-compute the modulus loop
    # boundary outside the loop.
    total = n + warmup
    for i in range(total):
        frame = frames[i % n_frames]
        t0 = monotonic_ns()
        pred_evaluate(frame)
        t_end = monotonic_ns()
        if i >= warmup:
            record(t_end - t0)


def _bench_plugin_source_parity(
    n: int,
    warmup: int,
    builtin_p99_ns: int,
) -> None:
    """Measure predicate evaluation latency for a plugin-registered source.

    Registers a fake plugin source named "circleci" (representative of a
    real third-party source), builds a predicate that matches on
    source="circleci", evaluates it against a pool of synthetic frames,
    and asserts that the resulting p99 latency is within
    _PLUGIN_SOURCE_LATENCY_TOLERANCE of the supplied built-in p99.

    The invariant being enforced: once a source name is registered via the
    plugin registry, the predicate engine's per-frame evaluation cost is
    the same whether the source is a built-in or a registered plugin. The
    registry lookup in is_known_source() is O(1) and the predicate engine
    operates on the frame dict directly, so cost parity is architectural —
    this bench detects regressions that would break that assumption.
    """
    from unittest.mock import MagicMock

    from waitbus.sources._protocol import SOURCE_PLUGIN_API_VERSION, SourceSpec
    from waitbus.sources._registry import _clear_for_test_isolation, register_plugin

    # Register the fake plugin source for this measurement window.
    _clear_for_test_isolation()
    plugin = MagicMock()
    plugin.spec.return_value = SourceSpec(
        name="circleci",
        event_types=("circleci_job",),
        api_version=SOURCE_PLUGIN_API_VERSION,
    )
    plugin.fetch.return_value = None
    ep = MagicMock()
    ep.name = "circleci"
    ep.value = "fake_circleci_pkg:circleci"
    ep.dist = None
    register_plugin(ep, plugin)

    pred = parse_match(['source="circleci"'])
    pred_evaluate = pred.evaluate
    monotonic_ns = time.monotonic_ns
    hdr = HdrRecorder()
    record = hdr.record

    # Build a pool of circleci frames so the bench does not repeat the same dict.
    rng = random.Random(_FRAME_GEN_SEED)
    pool_size = max(n + warmup, _DEFAULT_POOL_SIZE_FLOOR)
    frames = [_make_frame(rng, "circleci") for _ in range(pool_size)]
    n_frames = len(frames)

    total = n + warmup
    for i in range(total):
        frame = frames[i % n_frames]
        t0 = monotonic_ns()
        pred_evaluate(frame)
        t_end = monotonic_ns()
        if i >= warmup:
            record(t_end - t0)

    _clear_for_test_isolation()

    plugin_p99_ns = hdr.value_at_percentile_fraction(_P99_FRACTION)
    ceiling_ns = int(builtin_p99_ns * (1.0 + _PLUGIN_SOURCE_LATENCY_TOLERANCE))
    print(
        f"[{_BENCH_NAME}] plugin-source parity  "
        f"plugin_p99={plugin_p99_ns} ns  builtin_p99={builtin_p99_ns} ns  "
        f"ceiling={ceiling_ns} ns  "
        f"({'PASS' if plugin_p99_ns <= ceiling_ns else 'FAIL'})",
        file=sys.stderr,
    )
    if plugin_p99_ns > ceiling_ns:
        raise RuntimeError(
            f"plugin-source predicate latency regression: "
            f"plugin_p99={plugin_p99_ns} ns exceeds "
            f"builtin_p99={builtin_p99_ns} ns * "
            f"{1.0 + _PLUGIN_SOURCE_LATENCY_TOLERANCE} = {ceiling_ns} ns"
        )


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description="Predicate evaluation latency under mixed source load.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument("--n", type=int, default=_DEFAULT_N, help="number of measurement samples (default: 5000)")
    parser.add_argument(
        "--warmup", type=int, default=_DEFAULT_WARMUP, help="number of leading samples to discard (default: 500)"
    )
    parser.add_argument("--output", type=Path, default=None, help="path to write the result JSON")
    parser.add_argument("--smoke", action="store_true", help="quick run: N=500, warmup=50, no regression check.")
    parser.add_argument(
        "--check-regression",
        action="store_true",
        help=(
            f"after the run, compare p99 (gc-enabled) against "
            f"{_BASELINE_PATH.relative_to(_BASELINE_PATH.parent.parent)}; exit non-zero on >25% regression."
        ),
    )
    parser.add_argument("--no-gc-off", action="store_true", help="skip the gc-disabled companion run.")
    parser.add_argument(
        "--check-plugin-parity",
        action="store_true",
        help=(
            "after the main run, measure predicate latency for a plugin-registered source "
            "and assert p99 is within 25%% of the built-in-source p99."
        ),
    )
    args = parser.parse_args(argv)

    n = _SMOKE_N if args.smoke else args.n
    warmup = _SMOKE_WARMUP if args.smoke else args.warmup

    env = environment_report()
    print(f"[{_BENCH_NAME}] n={n} warmup={warmup}", file=sys.stderr)

    # Pre-build a pool of frames: 25%/25%/25%/25% across sources,
    # shuffled. Pool size is n + warmup so we don't repeat too
    # heavily over the bench window.
    rng = random.Random(_FRAME_GEN_SEED)
    pool_size = max(n + warmup, _WIDE_POOL_SIZE_FLOOR)
    per_source = pool_size // _SOURCE_COUNT
    frames: list[dict[str, object]] = []
    for source in _SOURCES:
        for _ in range(per_source):
            frames.append(_make_frame(rng, source))
    rng.shuffle(frames)

    started_at_ns = time.time_ns()
    hdr_main = HdrRecorder()
    hdr_gc_off: HdrRecorder | None = None if args.no_gc_off else HdrRecorder()

    print(f"[{_BENCH_NAME}] gc-on", file=sys.stderr)
    _run_loop(frames=frames, n=n, warmup=warmup, hdr=hdr_main)

    if hdr_gc_off is not None:
        print(f"[{_BENCH_NAME}] gc-off", file=sys.stderr)
        with gc_disabled():
            _run_loop(frames=frames, n=n, warmup=warmup, hdr=hdr_gc_off)

    ended_at_ns = time.time_ns()

    result = collect_result(
        bench_name=_BENCH_NAME,
        started_at_ns=started_at_ns,
        ended_at_ns=ended_at_ns,
        n_warmup_discarded=warmup,
        rate_hz=0.0,  # no rate -- pure CPU bench, no open-loop scheduler.
        hdr_main=hdr_main,
        hdr_gc_off=hdr_gc_off,
        environment=env,
        extra={
            "smoke": args.smoke,
            "frame_gen_seed": _FRAME_GEN_SEED,
            "frame_distribution": {s: per_source for s in _SOURCES},
            "predicate_source": _build_predicate().source,
        },
    )

    output_path = resolve_output_path(_BENCH_NAME, _RESULTS_DIR, args.output, env)

    write_result(result, output_path)
    print(f"[{_BENCH_NAME}] wrote {output_path}", file=sys.stderr)

    # Sub-microsecond values; report in nanoseconds.
    p50 = result.percentiles_gc_enabled["p50"]
    p90 = result.percentiles_gc_enabled["p90"]
    p99 = result.percentiles_gc_enabled["p99"]
    print(
        f"[{_BENCH_NAME}] gc-enabled  p50={p50.value_ns:6d} ns  "
        f"p90={p90.value_ns:6d} ns  "
        f"p99={p99.value_ns:6d} ns  "
        f"(n={result.n_samples})",
        file=sys.stderr,
    )
    if result.percentiles_gc_disabled is not None:
        g50 = result.percentiles_gc_disabled["p50"]
        g99 = result.percentiles_gc_disabled["p99"]
        print(
            f"[{_BENCH_NAME}] gc-disabled p50={g50.value_ns:6d} ns  p99={g99.value_ns:6d} ns",
            file=sys.stderr,
        )

    if args.check_regression and not args.smoke:
        ok, msg = check_regression(result, _BASELINE_PATH)
        print(f"[{_BENCH_NAME}] regression-check: {msg}", file=sys.stderr)
        if not ok:
            return 1

    if args.check_plugin_parity:
        builtin_p99_ns = result.percentiles_gc_enabled["p99"].value_ns
        print(f"[{_BENCH_NAME}] running plugin-source parity check", file=sys.stderr)
        try:
            _bench_plugin_source_parity(n=n, warmup=warmup, builtin_p99_ns=builtin_p99_ns)
        except RuntimeError as exc:
            print(f"[{_BENCH_NAME}] plugin-source parity FAILED: {exc}", file=sys.stderr)
            return 1

    return 0


if __name__ == "__main__":
    sys.exit(main())
